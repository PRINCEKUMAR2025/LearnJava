package datastructure;

public class Operators {
    public static void main(String[] args) {
       float a=20;
       float b=100;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a%b);
    }
}
